package BitwiseOperarion;
public class BinaryAND {
    public static void main(String[] args) {
        int a = 5;  // Binary: 0101
        int b = 6;  // Binary: 0110
        int result = a & b; // Binary AND operation

        System.out.println("The result of " + a + " & " + b + " is: " + result);
    }
    
}
